event = load_data("Data/CommonEvents.rvdata2")  
end_command = event[1].list.pop
if event[1].list[-1] && event[1].list[-1].code == 401
else
  event[1].list.push RPG::EventCommand.new(101,0,["",0,0,2])
end
text.each_line do |line|
  line.chomp!
  event[1].list.push RPG::EventCommand.new(401,0,[line])
end
while event[1].list[-1].parameters[0] == ""
  event[1].list.pop
end
event[1].list.push end_command
save_data(event,"Data/CommonEvents.rvdata2")

msgbox "导入完毕！#{$TEST ? "\n请重新启动编辑器以查看效果" : ""}"
