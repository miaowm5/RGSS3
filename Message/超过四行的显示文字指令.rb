【说明】

  使事件指令中的显示文字功能能够显示超过四行的文字
  
  在设定部分的两个“HEREHEREHERE”之间输入文字，之后运行游戏即可导入
  
  文字将被导入进公共事件1中，可以自由的复制到需要的地方
  编辑导入的文字则会被编辑器强制恢复成四行
  
  如果公共事件1的最后是一个显示文字的指令的话，
  导入的文字将自动接在这个指令显示的文字之后
  利用这一特点可以设置导入文字的脸图、窗口背景、位置……
  
=end
$m5script ||= {};$m5script[:M5IM20141006] = 20141006
